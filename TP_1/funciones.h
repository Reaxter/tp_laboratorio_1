#ifndef FUNCIONES_H_INCLUDED
#define FUNCIONES_H_INCLUDED

/**
 * Realiza la suma de dos operadodes pasados por parametro.
 * \param primer operador.
 * \param segundo operador.
 * \return el resultado de la suma del primer operador mas el segundo operador.
 */
int suma(int,int);

/**
 * Realiza la resta de dos operadodes pasados por parametro.
 * \param primer operador.
 * \param segundo operador.
 * \return el resultado de la resta del primer operador menos el segundo operador.
 */
int resta(int,int);

/**
 * Realiza la division de dos operadodes pasados por parametro.
 * \param primer operador (dividendo)
 * \param segundo operador (divisor)
 * \return el resultado de la division entre el primer operador dividido el segundo operador.
 */
float division(int,int);

/**
 * Realiza la multiplicacion de dos operadodes pasados por parametro.
 * \param primer operador.
 * \param segundo operador.
 * \return la multiplicacion resultado del primer operador por el segundo operador.
 */
int multiplicacion(int,int);

/**
 * Realiza el factorial de un operador pasado por parametro.
 * \param operador.
 * \return el factorial resultado del operador.
 */
int factorial(int);

#endif // FUNCIONES_H_INCLUDED
